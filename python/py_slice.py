""" slice """
friends = ['A', 'b', 'c', 'd']
#          0     1    2    3

print(friends[2:4])  # start, stop
print(friends[1:])  # from starting

friends2 = ['A', 'b', 'c', 'd', 'e', 'f']
print(friends2[:4])  # stop position

print(friends[:])  # new list or copy
print(friends[:-1])  # last

print(friends[-1])  # last
